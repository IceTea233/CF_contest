using namespace std;

typedef pair<int, int> pii;
const int INF = 1e18;
const int maxn = 2e5 + 7;

int ans[maxn] = {};

int32_t main() {
    cin.tie(0);
    int i,j;
    int t;

    cin >> t;
    while (t--) {
        int n;
        cin >> n;

        vector<pii> keep;
        for (int i=1; i<=n; i++) {
            int a;
            scanf("%lld", &a);
            keep.PB({a, i});
        }
        sort(all(keep), greater<pii>());
        int idx = 1;
        int sum = 0;
        for (auto it : keep) {
            ans[it.S] = idx;
            sum += abs(idx) * 2 * it.F;
            if (idx > 0)
                idx *= -1;
            else
                idx = -idx + 1;
        }

        printf("%lld\n", sum);
        for (int i=0; i<=n; i++) {
            printf("%lld ", ans[i]);
        }
        printf("\n");
    }

    return 0;
}

