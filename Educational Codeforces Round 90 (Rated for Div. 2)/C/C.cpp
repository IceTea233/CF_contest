using namespace std;

char s[1000007];

int main() {

    int i,j;
    int t;

    scanf("%d", &t);
    while(t--) {
        scanf("%s", s);

        LLI ans = 0;
        int cur = 0;
        for( i=0; s[i]; i++) {
            if( s[i] == '+')
                cur++;
            else
                cur--;

            if( cur < 0) {
                ans += i + 1;
                cur = 0;
            }
        }
        ans += i;
        printf("%lld\n", ans);
    }

    return 0;
}

