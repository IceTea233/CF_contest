int main() {

    int i,j;
    int t;

    cin >> t;
    while(t--) {
        int n;
        cin >> n;
        pii ans;
        int maxi = 1;
        for( int i=2; i*i<=n; i++) {
            if( n % i == 0) {
                maxi = n / i;
                break;
            }
        }
        ans = { maxi, n-maxi};
        cout << ans.fst << " " << ans.sec << "\n";
    }

    return 0;
}

