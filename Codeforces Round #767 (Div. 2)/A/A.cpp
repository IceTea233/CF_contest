using namespace std;

typedef pair<int, int> pii;
const int INF = 1e18;
const int maxn = 1e2 + 7;

int t;
int a[maxn] = {};
int b[maxn] = {};

int32_t main() {
    cin.tie(0);
    int i,j;
    int t;

    cin >> t;
    while (t--) {
        int n, k;
        cin >> n >> k;
        for (int i=1; i<=n; i++)
            cin >> a[i];
        for (int i=1; i<=n; i++)
            cin >> b[i];

        vector<pii> keep;
        for (int i=1; i<=n; i++) {
            keep.PB({a[i], b[i]});
        }
        sort(all(keep));

        int ans = k;
        for (auto it : keep) {
            if (ans >= it.F)
                ans += it.S;
            else
                break;
        }
        // cerr << "ans = ";
        printf("%lld\n", ans);
    }

    return 0;
}

